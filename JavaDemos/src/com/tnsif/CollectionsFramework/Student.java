package com.tnsif.CollectionsFramework;

public class Student {

	Integer id;
	String name;
	Float grade;
	
	public Student(Integer id, String name, Float grade) {
		this.id = id;
		this.name = name;
		this.grade = grade;
	}

	public String toString() {
		return "Student [id=" + id + ", name=" + name + ", grade=" + grade + "]";
	}
	
	
	
	
	
}
